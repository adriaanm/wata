/// 6×8 bitmap font renderer.
///
/// Source: font8x8 by Daniel Hepper — https://github.com/dhepper/font8x8
/// License: Public domain
///
/// The font8x8 data is embedded verbatim below in its native LSB-first
/// bit order (bit 0 = leftmost pixel). At comptime, each byte is
/// bit-reversed to MSB-first order for our renderer, and the full 128
/// ASCII entries are copied into a 256-entry table. Additional glyphs
/// (box drawing, arrows, etc.) can be added from font8x8_box.h and
/// other font8x8 tables in the same way.
const display = @import("display.zig");

/// Cell: 7px wide (6px glyph + 1px spacing gap), 8px tall.
pub const glyph_w: u32 = 7;
pub const glyph_h: u32 = 8;
const glyph_data_w: u32 = 6; // columns of actual glyph bitmap data
pub const cols: u32 = display.width / glyph_w; // 18
pub const rows: u32 = (display.height - 1) / glyph_h; // 19 (1px status line)

/// Draw a single character at pixel position (px, py).
pub fn drawChar(fb: *display.Framebuffer, ch: u8, px: i32, py: i32, fg: display.Color, bg: ?display.Color) void {
    const glyph = font_data[ch];
    var row: u32 = 0;
    while (row < glyph_h) : (row += 1) {
        const bits = glyph[row];
        var col: u32 = 0;
        while (col < glyph_data_w) : (col += 1) {
            const on = (bits >> @intCast(7 - col)) & 1 == 1;
            if (on) {
                fb.setPixel(px + @as(i32, @intCast(col)), py + @as(i32, @intCast(row)), fg);
            } else if (bg) |bg_color| {
                fb.setPixel(px + @as(i32, @intCast(col)), py + @as(i32, @intCast(row)), bg_color);
            }
        }
        // Spacing column: always background
        if (bg) |bg_color| {
            fb.setPixel(px + @as(i32, glyph_data_w), py + @as(i32, @intCast(row)), bg_color);
        }
    }
}

/// Returns true if pixel (col, row) is set for the given character.
pub fn getPixel(ch: u8, col: u32, row: u32) bool {
    if (col >= glyph_data_w or row >= glyph_h) return false;
    return (font_data[ch][row] >> @intCast(7 - col)) & 1 == 1;
}

/// Draw a string at character grid position (col, row).
/// Row 0 starts at y=1 (below the 1px status line).
pub fn drawText(fb: *display.Framebuffer, text: []const u8, col: u32, row: u32, fg: display.Color, bg: ?display.Color) void {
    for (text, 0..) |ch, i| {
        const x: i32 = @intCast((col + @as(u32, @intCast(i))) * glyph_w);
        const y: i32 = @intCast(1 + row * glyph_h);
        if (x + @as(i32, @intCast(glyph_w)) > @as(i32, @intCast(display.width))) break;
        drawChar(fb, ch, x, y, fg, bg);
    }
}

/// Draw text centered horizontally on the given row.
pub fn drawTextCentered(fb: *display.Framebuffer, text: []const u8, row: u32, fg: display.Color, bg: ?display.Color) void {
    const text_len: u32 = @intCast(text.len);
    const col = if (text_len >= cols) 0 else (cols - text_len) / 2;
    drawText(fb, text, col, row, fg, bg);
}

// ===========================================================================
// Comptime font pipeline: font8x8 (LSB-first) → bit-reverse → font_data
// ===========================================================================

/// Reverse the bit order of a single byte.
/// 0b_abcdefgh → 0b_hgfedcba
fn reverseBits(b: u8) u8 {
    var x = b;
    x = ((x & 0xF0) >> 4) | ((x & 0x0F) << 4);
    x = ((x & 0xCC) >> 2) | ((x & 0x33) << 2);
    x = ((x & 0xAA) >> 1) | ((x & 0x55) << 1);
    return x;
}

/// Convert a font8x8 table (LSB-first) to our MSB-first format at comptime.
fn convertFont8x8(comptime src: [128][8]u8) [128][8]u8 {
    var out: [128][8]u8 = undefined;
    for (0..128) |i| {
        for (0..8) |r| {
            out[i][r] = reverseBits(src[i][r]);
        }
    }
    return out;
}

/// Build the full 256-entry glyph table at comptime from font8x8 source tables.
const font_data: [256][8]u8 = blk: {
    @setEvalBranchQuota(10_000);
    var data: [256][8]u8 = [_][8]u8{[_]u8{0} ** 8} ** 256;

    // ASCII 0x00–0x7F from font8x8_basic
    const basic = convertFont8x8(font8x8_basic);
    for (0..128) |i| {
        data[i] = basic[i];
    }

    // Box drawing / block elements (0xB0–0xDF range in CP437)
    // These are symmetric or simple patterns — entered directly in MSB-first.
    data[0xDB] = .{ 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF }; // █
    data[0xB0] = .{ 0x44, 0x00, 0x11, 0x00, 0x44, 0x00, 0x11, 0x00 }; // ░
    data[0xB1] = .{ 0x55, 0xAA, 0x55, 0xAA, 0x55, 0xAA, 0x55, 0xAA }; // ▒
    data[0xB2] = .{ 0xBB, 0xFF, 0xEE, 0xFF, 0xBB, 0xFF, 0xEE, 0xFF }; // ▓

    break :blk data;
};

