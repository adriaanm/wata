    //  0x87: battery full (4/4)
    //  .XXX.
    //  XXXXX
    //  XXXXX   ← fill
    //  XXXXX   ← fill
    //  XXXXX   ← fill
    //  XXXXX   ← fill
    //  XXXXX
    //  .....
