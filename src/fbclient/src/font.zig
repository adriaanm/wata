/// Reverse the bit order of a single byte (for importing LSB-first fonts).
fn reverseBits(b: u8) u8 {
    var x = b;
    x = ((x & 0xF0) >> 4) | ((x & 0x0F) << 4);
    x = ((x & 0xCC) >> 2) | ((x & 0x33) << 2);
    x = ((x & 0xAA) >> 1) | ((x & 0x55) << 1);
    return x;
}

/// Convert a 128-entry LSB-first font table to MSB-first at comptime.
fn convertLsbFont(comptime src: [128][8]u8) [128][8]u8 {
    var out: [128][8]u8 = undefined;
    for (0..128) |i| {
        for (0..8) |r| {
            out[i][r] = reverseBits(src[i][r]);
        }
    }
    return out;
}

